package com.Gaokao.entity;

public class CollegeApplicationInfo {
    private int id;
    private int zsid;
    private int uid;
    private int px;

    public CollegeApplicationInfo() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getZsid() {
        return zsid;
    }

    public void setZsid(int zsid) {
        this.zsid = zsid;
    }

    public int getUid() {
        return uid;
    }

    public void setUid(int uid) {
        this.uid = uid;
    }

    public int getPx() {
        return px;
    }

    public void setPx(int px) {
        this.px = px;
    }
}
